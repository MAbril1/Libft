#include "libft.h"

int			ft_wordcount(char const *s, char c)
{
	int	count;
	int	i;

	i = 0;
	count = 0;
	if (!s)
		return (0);
	while (*(s + i))
	{
		if (*(s + i) == c && *(s + i + 1) != c)
		i++;
	}
	if (*(s + 0) != '\0')
		count++;
	return (count);
}
