#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	int i;
	int j;

	i = -1;
	j = ft_strlen(dst);
	while (*(src + (++i)))
		*(dst + (j++)) = *(src + i);
	*(dst + j) = '\0';
	return (dst);
}
