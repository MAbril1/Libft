#include "libft.h"

char	*ft_strncat(char *dst, char *src, size_t n)
{
	int i;
	int j;

	i = -1;
	j = ft_strlen(dst);
	while (*(src + (++i)) && i < (int)n)
		*(dst + (j++)) = *(src + i);
	*(dst + j) = '\0';
	return (dst);
}
